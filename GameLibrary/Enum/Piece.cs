namespace GameLibrary.Enum
{
    /// <summary>
    /// Represents a piece on a tile.
    /// </summary>
    public enum Piece
    {
        /// <summary>
        /// The piece is real
        /// </summary>
        Real,
        /// <summary>
        /// The piece is fake
        /// </summary>
        Fake,
        /// <summary>
        /// There is no piece on this tile
        /// </summary>
        Null
    }
}
